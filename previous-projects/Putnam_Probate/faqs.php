<h2>Frequently Asked Questions</h2>

<h3>WILLS</h3>

<p>Q: <em>Why do I have to probate the will when it states clearly everything is left to me?</em></p>

<p>A: Georgia like other states requires that a will must be probated to establish that it is the true and valid last will and testament of the deceased person.</p>

<p>Q. <em>What is the difference between Probate in Solemn Form and Probate in Common Form?</em></p>

<p>A. Probate in Solemn Form requires notice to the next of kin of the decedent. Probate In Common Form does not require that any notice be given but does not become conclusive as to the parties at interest until four years, perhaps even longer if an heir is a minor. For this reason by far most wills are probated in solemn form.</p>

<p>Q: <em>Why is it necessary to give notice of the petition to probate the will to next of kin who are not listed in the will to receive property?</em></p>

<p>A: Part of the process of proving that the will is valid depends on notice to the next of kin or heirs at law of the Decedent .As a result of notice they have the opportunity to receive a copy of the will and come to the hearing if they so desire.</p>

<p>Q: <em>How soon after the death of a loved one should the will be filed at the Probate Court?</em></p>

<p>A: Georgia law states that the will of a deceased person should be filed in the probate court of the county of residence of the decedent within a reasonable time.</p>


<h3>ADMINISTRATION</h3>

<p>Q. <em>My family member had no will, what and how will we manage the estate?</em></p>

<p>A. You can file a petition for letters of administration. The petition will list the heirs at law of the decedent whom the court will notify of the proceeding . The administrator may be selected by unanimous vote of all the heirs of the decedent. if a unanimous selection is not made the judge of the Probate Court must appoint that person who will serve the best interests of the estate. The approved administrator will handle the estate issues similarly to the executor of a will.</p>

<p>Q. <em>Do I need a lawyer to file a petition in the probate court?</em></p>

<p>A. The probate court is a self help court and there are standard forms available for you to fill out and file yourself. The judge and clerks of the probate court can provide some direction but not legal advice. If you need legal advice in order to proceed with your case you will need to retain an attorney.</p>

<h3>MARRIAGE</h3>

<p>Q. <em>Do we have to have a blood test to get a marriage license?</em></p>

<p>A. Blood tests are no longer required under Georgia law.</p>

<p>Q. <em>Can we get married at the courthouse the same day we get the license?</em></p>

<p>A. Yes, marriage ceremonies can be arranged by contacting the probate court. If you wait until you actually come in to apply for your license it will depend on the availability of the judge so we appreciate it if you will call in advance if you want to be married by the probate judge.</p>

<p>Q. <em>If I'm divorced why do I have to have a copy of my final decree of divorce when I apply for a marriage license?</em></p>

<p>A. Unfortunately it is required based on an experience where one applicant was not truthful in the past leading to serious consequences of fraud and criminal charges.</p>

<h3>BIRTH CERTIFICATES</h3>

<p>Q. <em>Can I get my birth certificate here even though I was born in a different county than Putnam?</em></p>

<p>A. Yes, if you were born in Georgia you should be able to obtain your birth certificate in any county in Georgia where vital records are issued.</p>

<p>Q. <em>What do I need to bring to get a certified copy of a birth certificate?</em></p>

<p>A. State Issued I.D. and cash or money order for $25.00.</p>


<h3>FIREARMS LICENSES</h3>

<p>Q. <em>I live in another county, can I still apply for my firearm license in Putnam County?</em></p>

<p>A. The firearms license must be applied for in the county of your permanent residence.</p>

<p>Q. <em>I was a resident of another county and have a license from that county that is good for two more years but now I've moved to Putnam County. What do I need to do?</em></p>

<p>A. Your license from the other county is still valid; when it is time for it to expire you will need to apply in Putnam County or your county of residence at that time.</p>

<p>Q. <em>When I renew my firearms license after five years will I have to redo my fingerprints?</em></p>

<p>A. Yes, Georgia law now requires that the fingerprinting be done each time you renew your Georgia firearms license.</p>

<p>Q. <em>Have the procedures changed in Georgia for obtaining a firearms license?</em></p>

<p>A. Yes, photographs are now on the Georgia firearms license.</p>
    
