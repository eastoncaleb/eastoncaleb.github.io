<h2>Guardianships</h2>

<h3>Guardianship of Incapacitated Adults</h3>

<p>This guardianship proceeding is filed when it is believed that an individual is unable to care for his or her personal needs and/or property. Such a case is usually initiated by the next of kin. A guardian may be appointed to care for the personal needs. A conservator may be needed to help make financial decisions and take care of financial issues. The individual may need both a guardian and a conservator. You can obtain standardized forms for this proceeding in the probate court and online at www.gaprobate.org at the Georgia Probate Court Online System.</p>

<p>Under certain circumstances a petitioner may file for an emergency guardianship/conservatorship . The Petitioner must present sufficient facts to support the claim for the need for the appointment of an emergency guardian and/or conservator.</p>

<h3>Guardians of Minors</h3>

<p>A permanent guardianship may be needed for a minor with no natural or testamentary guardian. Parents are the natural guardians of their children. Guardianships of minors may be needed due to death or disability of a parent. Often a temporary guardianship of a minor child will be granted so that the child lives with a relative or family friend for a period of time. Most temporary guardianships are granted with parental consent.</p>

<h3>Conservatorship of Minors</h3>

<p>Another guardianship proceeding, the Petition for Conservatorship of a minor is filed in the probate court. Such a case is filed when a minor receives property valued at more than $15,000.00.</p>

<h3>Annual Returns and Status Reports</h3>

<p>These reports are filed by the guardians and conservators of both minors and incapacitated adults. Most reports are filed annually. Forms for filing petitions, annual returns, and status reports are available in the probate court and online at www.gaprobate.org at the Georgia Probate Court Online System.</p>

