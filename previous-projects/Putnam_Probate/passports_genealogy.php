<h2>U.S. Passports and Genealogy</h2>

<h3>U.S. Passports</h3>

<p><img src="img/passport_book_sample.jpg" alt="Passport Book Sample" class="img" width="200" height="273" /><img src="img/passport_card_sample.jpg" alt="Passport Card Sample" class="img" width="414" height="273" /></p>


<p>The Probate Court of Putnam County is a United States Passport Acceptance Agency. We have passport forms and information available.</p>

<p>You can obtain information and forms online as well at www.travel.state.gov the website of the United States Department of the State. To apply for a passport you will need to bring one (1) passport photograph, identification, and a certified copy of your birth certificate to the Probate Court of Putnam County. The fee is $110.00 to the U.S. Department of the State and a $25.00 fee to the Acceptance Agent.</p>

<p>Recently the Department of the State began offering in addition to the Passport Book a Passport Card. At $30.00 the Passport Card is less expensive than the Passport Book which costs $110.00 . The Passport Card is limited to re-entry into the United States at land borders and sea ports of entry when traveling from Canada, Mexico, the Caribbean and Bermuda. <em>The Passport Card may not be used for air travel.</em></p>

<p>Routine Service for receiving a Passport Book or Card is six weeks. Both the Passport Book and Card application can be expedited for an additional fee of $60.00. Expedited service is within three weeks.</p>

<p><strong>Fees for minors are different:</strong></p>

<p><strong>Age 0-15 years</strong>. Passport book fee is $80.00 (check or money order made payable to U.S. Dept. of State) plus $25.00 execution fee to the Probate Court. A passport card is $15.00 (check or money order made payable to U.S. Dept. of State) as well as $25.00 execution fee to the Probate Court. Both parents as well as the minor must appear.</p>

<p><strong>Age 16+</strong>. Fees are the same as adults.</p>

<p><strong>Remember to apply for a Passport Book or Card you need to bring with you:</strong></p>

<ol>
<li>A valid government issued photo identification</li>
<li>The completed application (DS-11)</li>
<li>1 photo (photo must meet the guide as shown on the application)</li>
<li>Certified copy of birth certificate (will be returned by the Department of the State)</li>
</ol>

<p><strong>If you already have a U.S. Passport you may be able to apply using the Form DS-82 which you can mail directly and avoid paying the acceptance agent fee. You can renew with the DS-82 and save $25.00 if you can answer yes to the following:</strong></p>

<ul>

<li>I can submit my most recent U.S. passport book and/or U.S. passport card with this application.</li>

<li>I was at least 16 years old when my most recent U.S. passport book and/or passport card was issued.</li>

<li>I was issued my most recent U.S. passport book and/or passport card less than 15 years ago.</li>

<li>My most recent U.S. passport book and/or U.S. passport card for which I am renewing has not been lost, stolen, mutilated, or damaged.</li>

<li>I use the same name as on my recent U.S. passport book and/or U.S. passport card OR I have had my name changed by marriage or court order and can submit proper documentation to reflect my name change.</li>
</ul>

<h3>Genealogy</h3>

<p>Probate Courts in Georgia are one of the most important sites for genealogy resources. Among the resources on site in the Probate Court of Putnam County are:
</p>
    
<ul>
    <li>Estate records dating from 1808 forward</li>
    <li>Court records of proceedings from 1808 forward</li>
    <li>Marriage records from 1808</li>
    <li>Birth certificates from 1866</li>
    <li>Death certificates from 1919</li>
    <li>Cemetery compilations of county cemeteries</li>
</ul>
    
<p>Keep in mind that there are a limited number of birth certificates and death certificates on file for early years. Also access to all records is subject to the confidentiality provisions of Georgia law.
</p>
    
<p>You can also visit the Georgia Archives at: <a href="www.georgiaarchives.org">www.georgiaarchives.org</a></p>

