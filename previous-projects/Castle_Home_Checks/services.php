<h1 class="header">Services</h1>

<p><em>The following standard checks can be carried out on a weekly, bi-weekly or monthly basis as required.</em></p>

<p><strong>Exterior:</strong></p>
<ul>
<li>clear newspapers, flyers, mail</li>
<li>visual check of roof, plugged gutters or damaged drainpipes</li>
<li>visual checks on the outside windows and doors for forced entry or vandalism</li>
<li>check for visible signs of water or pest damage</li>
<li>visual check lawn for problems in the irrigation system</li>
<li>visual check of dock / boat</li>
<li>open and close the garage door to maintain door opener</li>
</ul>

<p><strong>Interior:</strong></p>
<ul>
<li>run water in all sinks and tubs</li>
<li>flush toilets and check for leaks to any visible plumbing</li>
<li>visual check ceilings, under sinks, and appliances which use water i.e. washing machine, dishwasher, fridge/freezer</li>
<li>check electric service panel for tripped breakers</li>
<li>monitor thermostats and seasonally adjust home temperature</li>
</ul>


