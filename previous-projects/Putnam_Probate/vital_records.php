<h2>Vital Records</h2>

<h3>Birth Records</h3>

<p>Birth certificates of those born in Putnam County are available in the Probate Court of Putnam County. Birth certificates are completed in the county where the person is actually born usually at the hospital. Birth records are also on file with the Georgia Vital Records Office in Atlanta, GA.</p>

<p>Birth records for persons born in any county in Georgia can now be accessed online in the Probate Court or vital records office of each county and a certified copy of the birth record can be generated. Birth Records and death records are confidential and except under very specific circumstances can be released only to the named individual or his or her next of kin.</p>

<h3>Death Records</h3>

<p>Death records for citizens domiciled in Putnam County at the time of death are available at the Probate Court as well. If the death occurs in another county the certificate is completed and filed in that county. A copy is forwarded to the county of residence after the death certificate is filed with the Georgia Vital Records Office. Death Certificates are usually completed by the director of the funeral home and delivered to the Probate Court.
</p>
    
<p>The Cost for a birth or death certificate is $25.00 for the first copy and $5.00 for each additional copy. <strong>Certified check, money order or cash is required for birth certificates.</strong></p>
    
<p>You can obtain additional information about vital records in Georgia at <a href="www.georgia.gov" target="_blank">www.georgia.gov</a>, the online site for Georgia Government.</p>

<p><strong>You must have a valid state issued identification card to obtain a vital record.</strong></p>
    
<h3>Amendments to Birth Records</h3>
    
<p>Forms are available in the Probate court to acknowledge paternity and to acknowledge legitimation of a child and to amend birth records with acceptable evidence.</p>

<p>Minor Corrections such as spelling corrections can be done in the Probate Court. A Petition is filed requesting the correction with sufficient evidence to support the change. Once the process is complete an order is signed by the Judge setting out the correction and forwarded to the Department of Vital Records to make the change to the birth record. Major changes such as changing a sur name on a birth certificate must be done through the Superior Court and additional requirements are necessary.</p>

