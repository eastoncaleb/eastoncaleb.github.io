<p>Temporary Protective Orders - document counseling for relief from family violence is available in the Probate Court. If you are a victim of abuse the Probate Judge or Clerk will help you determine if you are eligible for a protective order under the family violence act. O.C.G.A. ยง 19-13-3.</p>


<p>Once the documents are prepared you will be directed to a Superior Court Judge who will review them. Once signed by the Superior Court Judge you will return the original documents to be filed in the Office of the Clerk of Superior Court of Putnam County. Be sure you are given a copy of the order immediately when it is filed. Keep your copy with you so that you will have it in the event you need to call law enforcement. The Temporary Protective Order is a tool that aides law enforcement deputies and policeman in protecting you.</p>


<p>The order signed by the Judge in this situation is an ex parte order. That means the Judge heard only one party's presentation of the facts of the case. Once filed the case will be set down for a hearing. The hearing will be in 10 days or as soon as practical. At the hearing the petitioner who was granted the order is required to prove the allegations of the petition.</p>


