<h2>Licenses</h2>

<h3>Marriage Licenses</h3>

<p>Pursuant to Official Code of Georgia Section 19-3-2, the couple wanting to obtain a marriage license comes to the Probate Court and we will assist with the application. To be eligible for a marriage license in Georgia you must be at least 18 years old. If either applicant is 16 or 17 years of age, parental consent is required.</p>

<p>We require both bride and groom to be present to sign the application. They do not have to come in at the same time. The cost is $66.00 . Couples who have a certificate of completion showing that they have successfully completed a qualifying premarital education program as required by O.C.G.A. Section 19-3-30.1 pay only $26.00.</p>

<p>In order to obtain a marriage license the couple must present satisfactory identification (usually a Georgia Driver's License). If previously married a copy of the final judgment and decree of divorce signed by the judge must be provided.</p>

<p>Blood test are no longer a requirement in Georgia. The Probate Judge will perform marriage ceremonies upon request. If you want the Probate Judge to perform the marriage ceremony it is necessary to call first and make arrangements to ensure availability of the judge and facilities.</p>

<h3>Firearms Licenses</h3>

<p>Georgia Firearms Licenses are issued in the Probate Court pursuant to Official Code of Georgia Section 16-11-129. Applicants apply for the license in the Probate Court. An applicant must be at least 21 years of age and a legal resident of Putnam County. A photo I.D. is required. All applicants first time and renewal are required to be fingerprinted by the Putnam County Sheriff's Department for the necessary criminal history check.
</p>
    
